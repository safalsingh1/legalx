@asynccontextmanager
async def lifespan(app: FastAPI):
    """Run the AI pipeline on startup (background task)."""
    logger.info("LegalX server starting — launching AI pipeline in background...")
    asyncio.create_task(run_full_pipeline(force=False))
    yield
    logger.info("LegalX server shutting down.")


app = FastAPI(
    title="LegalX AI Knowledge Centre API",
    description="AI-powered legal knowledge platform using Grok (xAI)",
    version="1.0.0",
    lifespan=lifespan,
)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["http://localhost:5173", "http://localhost:3000", "*"],
    allow_credentials=True,
    allow_methods=["*"],
    allow_headers=["*"],
)


